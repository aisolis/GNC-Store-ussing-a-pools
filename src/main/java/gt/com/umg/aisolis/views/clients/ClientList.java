    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<Client> tmp = new ArrayList<>();
        if(!"".equals(clientNit.getText().trim())){
            tmp.add(clientDao.findById(clientNit.getText().trim()));
        }
        if(!"".equals(clientName.getText().trim())){
            tmp = clients.stream()
                    .filter(client -> client.getFullName().toUpperCase().contains(clientName.getText().trim().toUpperCase()))
                    .collect(Collectors.toList());
        }
        if("".equals(clientNit.getText().trim()) && "".equals(clientName.getText().trim())){
            tmp = clients;
        }
        initialiceTable(tmp);   
    }//GEN-LAST:event_jButton1ActionPerformed

