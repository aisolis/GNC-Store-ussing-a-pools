    private void initialiceTable(List<Client> clients){

        Object[][] data = new Object[clients.size()][5];
        for (int i = 0; i < clients.size(); i++) {
            Client p = clients.get(i);
            data[i][0] = p.getNit();
            data[i][1] = p.getFullName();
            data[i][2] = p.getAddress();
            data[i][3] = p.getCelphone();
            data[i][4] = p.getMail();
        }
        
        String[] columnNames = {"Nit", "Nombre del cliente", "Direccion del cliente", "Telefono del cliente", "E-mail del cliente"};

        DefaultTableModel model = new DefaultTableModel(data, columnNames);
       
        producttable.setModel(model);
    }
    
    
    
